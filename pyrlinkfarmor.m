%Calculate c0 for Farlie-Morgenstern model in Pyramid-linked
function [J c0]=pyrlinkfarmor(b,c)
A1=c(1)*b(1)*b(2)/2;
A2=c(2)*b(1)*(1-b(2))/2;
A3=c(3)*(1-b(1))*b(2)/2;
A4=c(4)*(1-b(1))*(1-b(2))/2;
B1=(b(1)+b(2)-b(1)*b(2))/(b(1)+b(2)-2*b(1)*b(2));
B2=(b(1)+(1-b(2))-b(1)*(1-b(2)))/(b(1)+(1-b(2))-2*b(1)*(1-
b(2)));
B3=((1-b(1))+b(2)-(1-b(1))*b(2))/((1-b(1))+b(2)-2*(1-
b(1))*b(2));
B4=((1-b(1))+(1-b(2))-(1-b(1))*(1-b(2)))/((1-b(1))+(1-b(2))-
2*(1-b(1))*(1-b(2)));
c0=(1-(A1*B1+A2*B2+A3*B3+A4*B4))/(b(2)-b(1))^2;
A0=1-(A1*B1+A2*B2+A3*B3+A4*B4);
J=[A1*B1 A2*B2 A3*B3 A4*B4 A0];